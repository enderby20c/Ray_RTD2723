//--------------------------------------------------
// Description  : Osd Load Hardware VLC Font
// Input Value  :
// Output Value :
//--------------------------------------------------
void OsdFontVLCLoadInputIcon(BYTE ucIconPos, WORD usIcon)
{
    BYTE ucOsdRotateStatus = _OSD_ROTATE_DEGREE_0;

#if((_OSD_ROTATE_FUNCTION == _OSD_ROTATE_SOFTWARE) ||\
    (_OSD_ROTATE_FUNCTION == _OSD_ROTATE_HARDWARE))
    ucOsdRotateStatus = GET_OSD_ROTATE_STATUS();
#endif

    if(usIcon >= _OPTION_ICON_END)
    {
        usIcon = usIcon - _OPTION_ICON_END;
    }

    OsdFontVLCLoadFontByGen(tFONT1_INPUT_ICON_TABLE[usIcon & 0x0F], VLC_TABLE_SIZE(tFONT1_INPUT_ICON_TABLE[usIcon & 0x0F]), GET_CURRENT_BANK_NUMBER(), (_1DYNAMIC_ICON_START + (ucIconPos * 24)), g_usFontTableStart, ucOsdRotateStatus);
}

